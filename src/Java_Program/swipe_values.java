package Java_Program;

public class swipe_values {
	public static void main(String[] args) {
		
		int x=2,y=4;
		
		 x=x+y;
		 y=x-y;
		 x=x-y;
		
		
		System.out.println(x);
		System.out.println(y);
		
		int a=3,b=6;
		
		a=a*b;
		b=a/b;
		a=a/b;
		
		System.out.println(a);
		System.out.println(b);
	}

}
